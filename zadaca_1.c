//1.bДа се напише програма за пресметување на сумата на сите парни двоцифрени броеви. Добиената сума се печати на екран

#include<stdio.h>
int main ()
{
    int n=11, sum=0;
    printf("%d",n);
    sum = n;
    n=n+2;
    for(n;n<99;n+=2){
        printf(" + %d",n);
        sum = sum + n;

    }
    printf("= %d\n",sum);
    return 0 ;

}